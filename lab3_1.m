% Практична робота №3 - Варіант 12
% Завдання 1: Побудова двох графіків в одному вікні

% Очищення робочого простору
clear all;
close all;
clc;

% Ввід вектора значень аргументу X
X = -5:1:5;

% Обчислення векторів значень функцій Y та Z
Y = 3*sin(X);
Z = 0.015*X.^3;

% Створення графічного вікна
figure('Name', 'Завдання 1 - Два графіки в одному вікні', 'NumberTitle', 'off');

% Побудова першого графіка функції Y із заданими параметрами
% (жовта лінія, маркери - зірочка)
plot(X, Y, 'y*-', 'LineWidth', 2, 'MarkerSize', 10);

% Додаємо до цього графіка графік функції Z
% (червона лінія, маркери - квадрати)
hold on;
plot(X, Z, 'rs-', 'LineWidth', 2, 'MarkerSize', 8);

% Подамо назву графіка
title('Побудова двох графіків в одному вікні (Варіант 12)', 'FontSize', 14);

% Вставимо підписи осей
xlabel('X', 'FontSize', 12);
ylabel('Y, Z', 'FontSize', 12);

% Додамо легенду
legend('y = 3sin(x)', 'z = 0.015x^3', 'Location', 'best', 'FontSize', 11);

% Побудуємо масштабні осі
grid on;

% Встановимо розмір шрифту для осей
set(gca, 'FontSize', 11);

% Додамо пояснювальний напис на малюнку
text(-4, 2, 'Жовта лінія - синусоїда', 'FontSize', 10, 'Color', 'black');
text(-4, 1.5, 'Червона лінія - кубічна функція', 'FontSize', 10, 'Color', 'black');

hold off;
